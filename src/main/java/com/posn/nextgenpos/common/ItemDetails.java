public class ItemDetails implements java.io.Serializable {
    private Integer id;
    private Integer quantity;

    public ItemDetails(Integer id, Integer quantity) {
        this.id = id;
        this.quantity = quantity;
    }

    public Integer getId() {
        return id;
    }

    public Integer getQuantity() {
        return quantity;
    }
    
}
